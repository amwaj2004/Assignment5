package edu.mu.mvc.adapter;

/**
 * A third-party ExoticAnimal class
 */
public class ExoticAnimal {
	String id;
	String name;
	String catagory;
	String subSpecies;
	int age;
	
	public String getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	public String getCatagory() {
		return catagory;
	}
	public String getSubSpecies() {
		return subSpecies;
	}
	public int getAge() {
		return age;
	}

	
}
